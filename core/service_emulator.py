    async def handle_connection(
        self,
        context: ConnectionContext,
        raw_data: str | bytes,
    ) -> EmulatedResponse:
        """Handle HTTP requests — parse method/path and route appropriately."""
        data = raw_data.decode() if isinstance(raw_data, bytes) else raw_data

        # Parse HTTP request line
        request_line = data.split("\n")[0].strip()
        parts = request_line.split()

        if len(parts) < 2:
            return self._error_response(400, "Bad Request")

        method = parts[0].upper()
        path = parts[1] if len(parts) > 1 else "/"

        # Log the request
        self._log_http_request(context, method, path, data)

        # Route to appropriate handler
        if path in self._admin_pages:
            return EmulatedResponse(
                payload=self._admin_pages[path],
                response_type="html",
                status_code=200,
                headers=self._http_headers({"Content-Type": "text/html"}),
            )

        if path in self._api_endpoints:
            endpoint = self._api_endpoints[path]
            if method in endpoint.get("methods", ["GET"]):
                resp_body = endpoint.get("response", {})
                if isinstance(resp_body, dict):
                    # Inject timestamp
                    if "timestamp" in resp_body:
                        resp_body["timestamp"] = datetime.now(timezone.utc).isoformat()
                    resp_body = json.dumps(resp_body)
                return EmulatedResponse(
                    payload=resp_body,
                    response_type="json",
                    status_code=200,
                    headers=self._http_headers({
                        "Content-Type": "application/json",
                        "X-RateLimit-Limit": "1000",
                        "X-RateLimit-Remaining": "999",
                    }),
                )
            return self._error_response(405, "Method Not Allowed")

        # Common paths
        if path == "/" or path == "/index.html":
            return self._default_homepage()

        if path == "/login":
            return self._login_page(data)

        if path.startswith("/api/"):
            return self._error_response(404, f"API endpoint '{path}' not found")

        if path.startswith("/.env") or path.startswith("/config") or path.startswith("/backup"):
            # Tempting paths — return fake leaked data
            return self._bait_response(path)

        # Default 404
        return self._error_response(404, f"The requested URL {path} was not found on this server.")

