# ── Data Models ──────────────────────────────────────────────────────────


class ProtocolType(str, Enum):
    """Supported protocol types for emulation."""

    SSH = "ssh"
    HTTP = "http"
    FTP = "ftp"
    DATABASE = "database"


@dataclass
class ConnectionContext:
    """Context for an incoming connection to an emulated service."""

    source_ip: str
    source_port: int
    destination_port: int
    connected_at: datetime = field(default_factory=lambda: datetime.now(timezone.utc))
    session_id: str = ""
    authenticated_user: str | None = None
    working_directory: str = "/home/user"
    environment: dict[str, str] = field(default_factory=dict)
    metadata: dict[str, Any] = field(default_factory=dict)


@dataclass
class EmulatedResponse:
    """Response generated by an emulated service."""

    payload: str | bytes
    response_type: str = "text"  # text | binary | json | html
    status_code: int = 200
    headers: dict[str, str] = field(default_factory=dict)
    delay_ms: int = 0  # Simulated processing delay


# ── Abstract Base ────────────────────────────────────────────────────────


class ServiceEmulator(ABC):
    """Abstract base class for service emulators.

    Each emulator implements the protocol-level behavior needed to
    convincingly simulate a real service while logging all interactions.
    """

    protocol: ProtocolType = ProtocolType.SSH
    banner: str = ""
    version_string: str = ""

    def __init__(
        self,
        banner: str | None = None,
        version_string: str | None = None,
        config: dict[str, Any] | None = None,
    ) -> None:
        self.banner = banner or self.__class__.banner
        self.version_string = version_string or self.__class__.version_string
        self.config = config or {}
        self._running = False
        self._connection_count = 0
        self._command_log: list[dict[str, Any]] = []

    # ── Lifecycle ─────────────────────────────────────────────────────────

    async def start(self) -> None:
        """Start the emulator. Override for protocol-specific startup."""
        self._running = True
        logger.info("%s emulator started (banner: %s)", self.protocol.value, self.banner)

    async def stop(self) -> None:
        """Stop the emulator gracefully."""
        self._running = False
        logger.info("%s emulator stopped", self.protocol.value)

    # ── Abstract Interface ────────────────────────────────────────────────

    @abstractmethod
    async def handle_connection(
        self,
        context: ConnectionContext,
        raw_data: str | bytes,
    ) -> EmulatedResponse:
        """Handle an incoming connection/data from an attacker.

        Args:
            context: Connection metadata (IP, port, session info).
            raw_data: The raw data sent by the attacker.

        Returns:
            EmulatedResponse to send back to the attacker.
        """
        ...

    @abstractmethod
    async def get_banner(self) -> str:
        """Return the service banner string."""
        ...

    # ── Shared Helpers ────────────────────────────────────────────────────

    def _log_command(
        self,
        context: ConnectionContext,
        command: str,
        response: str,
    ) -> None:
        """Log a command for forensic analysis."""
        entry = {
            "timestamp": datetime.now(timezone.utc).isoformat(),
            "source_ip": context.source_ip,
            "session_id": context.session_id,
            "protocol": self.protocol.value,
            "command": command,
            "response_preview": response[:200] if response else "",
            "authenticated_user": context.authenticated_user,
        }
        self._command_log.append(entry)
        self._connection_count += 1

    @property
    def is_running(self) -> bool:
        return self._running

    @property
    def connection_count(self) -> int:
        return self._connection_count

    def get_logs(self, limit: int = 100) -> list[dict[str, Any]]:
        """Return recent command logs."""
        return self._command_log[-limit:]


