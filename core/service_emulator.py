    def _log_http_request(
        self,
        context: ConnectionContext,
        method: str,
        path: str,
        raw_data: str,
    ) -> None:
        """Log HTTP request details."""
        entry = {
            "timestamp": datetime.now(timezone.utc).isoformat(),
            "source_ip": context.source_ip,
            "method": method,
            "path": path,
            "raw_request": raw_data[:500],
            "session_id": context.session_id,
        }
        self._request_log.append(entry)
        self._connection_count += 1
        super()._log_command(context, f"{method} {path}", raw_data[:200])

    def _http_headers(self, extra: dict[str, str] | None = None) -> dict[str, str]:
        """Generate standard HTTP headers with Date and Server."""
        headers = {
            "Date": datetime.now(timezone.utc).strftime("%a, %d %b %Y %H:%M:%S GMT"),
            "Server": self.version_string,
        }
        if extra:
            headers.update(extra)
        return headers

    def _error_response(self, code: int, message: str) -> EmulatedResponse:
        """Generate an HTML error page."""
        html = (
            f"<!DOCTYPE html>\n"
            f"<html><head><title>{code} {message}</title></head>\n"
            f"<body><h1>{code} {message}</h1>\n"
            f"<p>The server encountered an error processing your request.</p>\n"
            f"<hr><address>{self.version_string}</address></body></html>"
        )
        return EmulatedResponse(
            payload=html,
            response_type="html",
            status_code=code,
            headers=self._http_headers({"Content-Type": "text/html"}),
        )

