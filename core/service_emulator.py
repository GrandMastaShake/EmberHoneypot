    def _handle_scp(self, command: str) -> str:
        """Return realistic SCP file transfer simulation."""
        parts = command.split()
        filename = parts[1] if len(parts) > 1 else "file.txt"
        file_size = len(filename) * 1024 + 512
        return (
            f"{filename}                                    100%  {file_size}B   "
            f"{file_size / 1024:.1f}KB/s   00:00\n"
        )

    def _handle_ssh_escape(self, command: str) -> str:
        """Handle SSH escape sequences like ~. (disconnect) and ~^Z (suspend)."""
        if command == "~.":
            return "Connection closed by escape sequence.\r\n"
        if command == "~^Z":
            return "Suspended by escape sequence. Use 'fg' to resume.\r\n"
        if command.startswith("~"):
            return f"Unknown escape sequence '{command}'. Supported: ~. ~^Z\r\n"
        return ""

